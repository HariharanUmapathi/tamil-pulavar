            <p class="text-justify">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;அல்டிமேட் மென்பொருள் தீர்வகம் திண்டுக்கல்லைத் தலைமையிடமாகக் கொண்டு நான்கைந்து தனிநபர்களின் துணையுடன் இயங்கிவரும் அமைப்பாகும்.தொடர்ந்து 15 வருடங்களாக தமிழ் மென்பொருள் உருவாக்கத்தில் தன்னை அர்ப்பணித்து இயங்கி வரும் அமைப்பு இது.</p><br />
            <p class="text-justify"> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
               பல்கலைக் கழகங்கள் போன்ற மாபெரும் நிறுவனங்கள் செய்திட வேண்டிய இந்தச் செயலை மிகக் கடினமான உழைப்பினால் செய்து வருகிறோம்.
               ஒவ்வொரு காலத்திலும் வெளியான அகராதிகளைத் தொகுத்திடும் போது மொழியின் கால அளவு மாற்றங்களை எளிதில் மொழி ஆய்வாளர்கள் ஆய்வு செய்ய வசதி அளிக்கும் நோக்கில் இந்தத் தளம் வடிவமைக்கப் பட்டுள்ளது.
               கீழே தரப்பட்டுள்ள அட்டவணை இதுவரை வெளியான அகராதிகளின் தொகுப்பாகும். இது மட்டுமின்றி மருத்துவம், சட்டம் மற்றும் பல்வேறு துறை சார்ந்த அகராதிகளையும் இணைக்க வேண்டிய தேவை இருக்கிறது.</p><br />
